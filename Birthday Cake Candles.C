#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main()
{
int n,max=0 ,count=0;
scanf("%d\n",&n);

    int candles[n];
    
    for(int i=0 ;i<n ;i++)
        {
         scanf("%d", &candles[i]);  
            if (candles[i]> max)
            {
                max = candles[i];
            }
        }
    for(int i=0 ;i<n ;i++)
        {
            if (candles[i] == max)
            {
                count +=1;
            }
        }
    
    printf("%d",count);
return 0; 
}
